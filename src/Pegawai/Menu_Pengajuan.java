    private void b_processActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_processActionPerformed
        switch (b_process.getText()){
            case "Buat Baru":
                this.create_new_pengajuan();
                this.enabled_form(true);
                b_process.setText("Simpan");
                b_cancel.setText("Batal");
                break;
            case "Simpan":
                this.save_pengajuan();
                this.enabled_form(false);
                this.clear();
                b_process.setText("Buat Baru");
                b_cancel.setText("Keluar");
                this.enabled_form(false);
                this.get_data_table();
                this._id = 0;
                break;
        }
    }//GEN-LAST:event_b_processActionPerformed

    private void b_cancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_cancelActionPerformed
        switch (b_cancel.getText()){
            case "Batal":
                int konfir = JOptionPane.showConfirmDialog(this, 
                    "Apa Anda Yakin?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            
                if (konfir == JOptionPane.YES_OPTION){

                    try {
                        db.runQueryUpdate("DELETE FROM data_pengajuan WHERE id_pengajuan = '"+ this._id +"'");
                        koneksi.popup_message("Berhasil di hapus!");
                    } catch (SQLException err) {koneksi.print(err.getMessage());}

                }
                this.clear();
                this.enabled_form(false);
                b_cancel.setText("Keluar");
            break;
            case "Keluar":
                try {
                    db.runQueryUpdate("DELETE FROM sessions WHERE username = '"+f_id_pegawai.getText()+"'");
                    koneksi.popup_message("Anda Telah Logout");
                } catch (SQLException err) { koneksi.print(err.getMessage()); }
                this.dispose();
                break;
        }
    }//GEN-LAST:event_b_cancelActionPerformed

